//adição
console.log(10 + 5);

//subtração
console.log(10 - 5);

//multiplicação
console.log(10 * 5);

//divisão
console.log(10 / 5);

//incremento
let numero = 10;
console.log(++numero);

//decremento - utilizando a variável definide no incremento
console.log(--numero)

//comparação de valores, maior
console.log(10 > 5);

//comparação de valores, maior ou igual
console.log(10 >= 5);

//comparação de valores, menor
console.log(10 < 5);

//comparação de valores, menor ou igual
console.log(10 <= 5);

//comparação de igualdade de valores
console.log(10 == 5);

//comparação de igualdade de valores rígido
console.log(5 === "5");

//comparação de valores diferentes
console.log(5 != "5");

//comparação de valores diferentes rígidos
console.log(5 !== "5");

//comparador lógico AND &&
console.log(10 > 5 && 8 < 10 && 10 === "10");

//comparador lógico OR ||
console.log(10 > 5 || 8 < 10 || 10 === 10);

//comparador de negação ! 
console.log(!10 > 5 );

//concatenação
let nome = "Moisés";
let sobrenome = "Ribeiro";
console.log(nome +" "+sobrenome);


